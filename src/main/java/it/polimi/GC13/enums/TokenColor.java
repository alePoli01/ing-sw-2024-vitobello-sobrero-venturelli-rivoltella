package it.polimi.GC13.enums;

/**
 * Enum representing different colors of playable tokens.
 * <ul>
 *     <li>{@link #GREEN} - Represents a green token.</li>
 *     <li>{@link #BLUE} - Represents a blue token.</li>
 *     <li>{@link #YELLOW} - Represents a yellow token.</li>
 *     <li>{@link #RED} - Represents a red token.</li>
 * </ul>
 * Used to categorize tokens based on their color for various game mechanics.
 */
public enum TokenColor {
    /**
     * Represents a green token.
     */
    GREEN,

    /**
     * Represents a blue token.
     */
    BLUE,

    /**
     * Represents a yellow token.
     */
    YELLOW,

    /**
     * Represents a red token.
     */
    RED
}
