package it.polimi.GC13.enums;

/**
 * Enum representing the types of playable cards in the game.
 * <ul>
 *     <li>{@link #GOLD} - Gold cards.</li>
 *     <li>{@link #RESOURCE} - Resource cards.</li>
 *     <li>{@link #STARTER} - Starter cards.</li>
 * </ul>
 */
public enum CardType {
    /**
     * Represents a gold card.
     */
    GOLD,

    /**
     * Represents a resource card.
     */
    RESOURCE,

    /**
     * Represents a starter card.
     */
    STARTER
}
